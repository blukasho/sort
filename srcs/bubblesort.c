#include <swap.h>

void		bubblesort(int v[], int n)
{
	int		i;
	int		l;
	int		tmp;

	i = 0;
	while (i < n)
	{
		l = i;
		while (l < n)
		{
			if (v[i] > v[l])
				swap(v, i, l);
			++l;
		}
		++i;
	}
}
